export function getFrameForZone(zone, seen = []) {
  return STORY_FRAMES.find(f => f.trigger?.zone === zone && !seen.includes(f.id))
}
export function getFrameById(id) {
  return STORY_FRAMES.find(f => f.id === id) || null
}
